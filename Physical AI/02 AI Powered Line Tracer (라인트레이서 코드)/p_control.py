# p_control.py
# PAI-Car v1.0 P 제어 라인트레이싱 예제
#
# 이 코드는 PAI-Car를 트랙 위에 올려놓고,
# 라인센서 값을 이용하여 검은색 라인을 따라가도록 주행시키는 코드이다.
#
# 전체 동작 순서:
#   1. PAI-Car 하드웨어 준비
#   2. 라인센서 셀프 캘리브레이션 수행
#   3. 사용자 버튼을 눌렀다가 떼면 출발
#   4. while 반복문 안에서 P 제어로 라인트레이싱 수행
#
# 참고:
#   자주 바꾸지 않는 하드웨어 설정, 센서 설정, 캘리브레이션 설정,
#   버튼 처리, 제어 주기 맞추기 등은 pai_car_run_support.py 파일에
#   기본값으로 정리되어 있다.
#
#   이 파일에서는 학생들이 주로 조정해 볼 만한 값과
#   P 제어의 핵심 흐름만 보이도록 구성하였다.


from time import ticks_ms

from pai_car_run_support import (
    setup_paicar,
    self_calibrate_or_stop,
    wait_button_start,
    read_line_error,
    limit_cmd,
    wait_control_period,
)


# ------------------------------------------------------------
# P-control settings
# ------------------------------------------------------------
# 아래 두 값은 P 제어 라인트레이싱에서 가장 기본적으로 조정해 볼 수 있는 값이다.

# BASE_SPEED:
#   PAI-Car가 기본적으로 앞으로 나아가려는 속도이다.
#
#   값이 커질수록 차가 더 빠르게 주행한다.
#   하지만 너무 크게 설정하면 코너에서 라인을 벗어나기 쉽다.
#
#   값이 작을수록 차가 천천히 주행한다.
#   처음 테스트할 때는 너무 빠르게 하지 않는 것이 좋다.
BASE_SPEED = 300


# KP:
#   P 제어에서 error에 곱해지는 비례 제어 계수이다.
#
#   error는 라인의 중심에서 차가 얼마나 벗어났는지를 나타내는 값이다.
#   KP가 클수록 라인에서 벗어났을 때 더 강하게 방향을 수정한다.
#
#   KP가 너무 작으면:
#       방향 수정이 약해서 라인을 천천히 따라가거나,
#       코너에서 바깥쪽으로 밀려날 수 있다.
#
#   KP가 너무 크면:
#       방향 수정이 지나치게 강해져서 차가 좌우로 흔들릴 수 있다.
#
#   따라서 BASE_SPEED와 KP는 함께 조정해야 한다.
KP = 0.11


# ------------------------------------------------------------
# Setup
# ------------------------------------------------------------
# PAI-Car 주행에 필요한 장치들을 준비한다.
#
# setup_paicar() 함수 안에서는 다음 작업이 수행된다.
#   - TLA2528 ADC 초기화
#   - 라인센서 객체 생성
#   - 모터 객체 생성
#   - 사용자 버튼 설정
#
# 복잡한 하드웨어 초기화 코드는 학생용 메인 코드에서 숨기고,
# 이 파일에서는 "PAI-Car를 준비한다"는 흐름만 보이도록 하였다.

line, motors, button = setup_paicar()


# ------------------------------------------------------------
# Self calibration
# ------------------------------------------------------------
# 라인센서 셀프 캘리브레이션을 수행한다.
#
# 캘리브레이션은 각 라인센서가 흰색 바닥과 검은색 라인을
# 어느 정도 값으로 읽는지 기준을 잡는 과정이다.
#
# self_calibrate_or_stop() 함수 안에서는 PAI-Car가 짧게 좌우로 움직이며
# 라인센서의 최소값/최대값을 측정하고, 그 결과를 line_cal.json 파일에 저장한다.
#
# 캘리브레이션이 실패하면 안전을 위해 주행하지 않고 정지 상태를 유지한다.
#
# 캘리브레이션 속도, 회전 시간, 샘플링 간격 같은 세부 설정은
# pai_car_run_support.py 파일에 기본값으로 정리되어 있다.

self_calibrate_or_stop(line, motors)


# ------------------------------------------------------------
# Button start
# ------------------------------------------------------------
# 셀프 캘리브레이션이 끝나면 바로 출발하지 않고,
# 사용자가 버튼을 눌렀다가 뗄 때까지 기다린다.
#
# 이렇게 하면 PAI-Car를 트랙의 시작 위치에 안정적으로 올려놓은 뒤
# 원하는 순간에 출발시킬 수 있다.

wait_button_start(button)


# ------------------------------------------------------------
# P-control line tracing
# ------------------------------------------------------------
# 여기부터가 실제 라인트레이싱 주행 코드이다.
#
# while True:
#   이 반복문은 PAI-Car가 계속해서 라인을 읽고,
#   모터 속도를 조정하면서 주행하도록 만든다.
#
# 반복문 안의 핵심 흐름:
#   1. 라인센서로부터 error 값을 읽는다.
#   2. 라인을 정상적으로 감지했는지 확인한다.
#   3. error에 KP를 곱해 correction 값을 계산한다.
#   4. correction 값을 이용해 왼쪽/오른쪽 모터 속도를 다르게 만든다.
#   5. 계산된 속도로 모터를 구동한다.
#   6. 일정한 제어 주기가 되도록 기다린다.

try:
    while True:
        # 현재 반복이 시작된 시간을 저장한다.
        #
        # 이 값은 반복문 마지막에서 제어 주기를 일정하게 맞추는 데 사용된다.
        loop_start = ticks_ms()


        # 라인센서 값을 읽고 error와 on_line 값을 얻는다.
        #
        # error:
        #   라인의 중심에서 PAI-Car가 얼마나 벗어났는지를 나타내는 값이다.
        #
        #   error가 0에 가까우면:
        #       차가 라인의 중심 근처에 있다는 뜻이다.
        #
        #   error가 양수 또는 음수이면:
        #       차가 라인 중심에서 한쪽으로 벗어났다는 뜻이다.
        #
        # on_line:
        #   라인을 정상적으로 감지했는지 여부를 나타낸다.
        #
        #   True이면:
        #       라인을 감지한 상태이다.
        #
        #   False이면:
        #       라인을 잃어버렸다고 판단한 상태이다.
        error, on_line = read_line_error(line)


        # 라인을 정상적으로 감지한 경우에만 P 제어를 수행한다.
        if on_line:

            # P 제어의 핵심 계산이다.
            #
            # correction = KP × error
            #
            # error가 크면 라인 중심에서 많이 벗어난 것이므로
            # correction 값도 커진다.
            #
            # correction 값이 커질수록 왼쪽/오른쪽 모터 속도 차이가 커지고,
            # 차는 더 강하게 방향을 수정한다.
            correction = int(KP * error)


            # 왼쪽 모터 속도와 오른쪽 모터 속도를 계산한다.
            #
            # 기본 속도(BASE_SPEED)에 correction 값을 더하거나 빼서
            # 양쪽 바퀴의 속도 차이를 만든다.
            #
            # 예를 들어 왼쪽 속도가 커지고 오른쪽 속도가 작아지면
            # 차는 오른쪽으로 회전하려는 움직임을 갖는다.
            #
            # 반대로 왼쪽 속도가 작아지고 오른쪽 속도가 커지면
            # 차는 왼쪽으로 회전하려는 움직임을 갖는다.
            left_speed = limit_cmd(BASE_SPEED + correction)
            right_speed = limit_cmd(BASE_SPEED - correction)


            # 계산된 속도로 왼쪽/오른쪽 모터를 구동한다.
            motors.drive(left_speed, right_speed)


        # 라인을 정상적으로 감지하지 못한 경우
        else:
            # 첫 번째 P 제어 테스트에서는 라인을 잃으면 정지하도록 한다.
            #
            # 나중에 더 발전된 코드에서는
            # 마지막으로 라인을 본 방향으로 회전하며 라인을 다시 찾는 방식도 가능하다.
            motors.stop()


        # 제어 주기를 일정하게 맞춘다.
        #
        # 예를 들어 pai_car_run_support.py에 CONTROL_MS = 10으로 설정되어 있다면,
        # 이 while 반복문은 약 10 ms마다 한 번씩 실행되도록 조정된다.
        #
        # 제어 주기를 일정하게 유지하면
        # 센서 읽기와 모터 제어가 더 안정적으로 동작한다.
        wait_control_period(loop_start)


finally:
    # 프로그램이 중간에 멈추거나 오류가 발생하더라도
    # 마지막에는 반드시 모터를 정지시킨다.
    #
    # 안전을 위한 코드이다.
    motors.stop()